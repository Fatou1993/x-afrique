              <div align="justify">
                Le forum <strong>X-Afrique 2017</strong> organise la rencontre entre différents acteurs du développement africain : start-ups, investisseurs et partenaires économiques, mais aussi chercheurs, personnalités politiques et laboratoires d’idées. </br></br>
                Pour sa deuxième édition, le forum se tiendra le <strong>samedi 25 février 2017</strong> dans le <strong>Grand Hall de l’École Polytechnique</strong>. Cette année, nous nous rassemblons autour d’un défi passionnant :</br></br>
                <div align="center" id="theme">
                  <strong>« L’innovation au service du développement rural en Afrique »</strong>
                </div>
                </br>
                L’Afrique, notamment subsaharienne, connaît une croissance démographique exceptionnelle depuis plusieurs années, caractérisée par une forte augmentation de sa population urbaine.
                Ce développement des villes est lié à une croissance économique importante dans certains pays ; il favorise aussi l’insécurité sociale et ne va pas toujours dans le sens d’un développement juste et équitable.
                Quels sont les outils technologiques qui permettront aux entrepreneurs et aux investisseurs en Afrique de participer au développement des zones rurales, et aux États de mener une politique efficace d’intégration économique de toute la population ?
                Comment désenclaver les territoires les plus reculés au moyen des technologies les plus avancées ?</br></br>
                Nous réservons à nos invités des conférences de qualités, des ateliers de sensibilisation à la dynamique entrepreneuriale en Afrique et des sessions de coaching sur le lancement d'un business à fort impact transformant pour les populations africaines.
                Par ailleurs, l'incubateur de l'Ecole Polytechnique recrute de jeunes porteurs de projets et occupera un stand lors de ce forum.</br></br>
